//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
HTTPManager::init()
{
    curl_global_init(CURL_GLOBAL_ALL);

    m_pCurlHandle = curl_easy_init();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
HTTPManager::cleanup()
{
    curl_easy_cleanup(m_pCurlHandle);
    curl_global_cleanup();
    m_pCurlHandle = NULL;
}

